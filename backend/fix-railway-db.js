// سكريبت لإصلاح عمود customers.id في قاعدة بيانات Railway
// يمكن تشغيله مباشرة من لوحة تحكم Railway

const { Pool } = require('pg');

// إنشاء اتصال بقاعدة البيانات
const pool = new Pool({
  connectionString: process.env.DATABASE_URL,
  ssl: { rejectUnauthorized: false }
});

async function fixCustomersTable() {
  console.log('🔍 جاري فحص جدول العملاء...');
  
  const client = await pool.connect();
  try {
    // التحقق من وجود جدول customers
    const tableExists = await client.query(`
      SELECT EXISTS (
        SELECT FROM information_schema.tables 
        WHERE table_schema = 'public' 
        AND table_name = 'customers'
      );
    `);
    
    if (!tableExists.rows[0].exists) {
      console.log('❌ جدول العملاء غير موجود!');
      return;
    }
    
    console.log('✅ جدول العملاء موجود');
    
    // التحقق من حالة عمود id
    const columnInfo = await client.query(`
      SELECT is_identity, column_default 
      FROM information_schema.columns 
      WHERE table_schema = 'public' 
      AND table_name = 'customers' 
      AND column_name = 'id';
    `);
    
    if (columnInfo.rows.length === 0) {
      console.log('❌ عمود id غير موجود في جدول العملاء!');
      return;
    }
    
    const isIdentity = columnInfo.rows[0].is_identity === 'YES';
    const hasSequence = columnInfo.rows[0].column_default && 
                        columnInfo.rows[0].column_default.includes('nextval');
    
    console.log('حالة عمود id الحالية:');
    console.log(`- is_identity: ${isIdentity ? 'YES' : 'NO'}`);
    console.log(`- column_default: ${columnInfo.rows[0].column_default || 'NULL'}`);
    
    if (isIdentity) {
      console.log('✅ عمود id مهيأ بالفعل كـ IDENTITY');
      return;
    }
    
    if (hasSequence) {
      console.log('✅ عمود id مهيأ بالفعل باستخدام sequence');
      return;
    }
    
    // إصلاح العمود بإضافة IDENTITY
    console.log('🔧 جاري إضافة IDENTITY إلى عمود id...');
    
    await client.query('BEGIN');
    
    try {
      await client.query(`
        ALTER TABLE customers 
        ALTER COLUMN id ADD GENERATED BY DEFAULT AS IDENTITY;
      `);
      
      await client.query('COMMIT');
      console.log('✅ تم إضافة IDENTITY إلى عمود id بنجاح');
      
      // التحقق من التغيير
      const verifyInfo = await client.query(`
        SELECT is_identity, column_default 
        FROM information_schema.columns 
        WHERE table_schema = 'public' 
        AND table_name = 'customers' 
        AND column_name = 'id';
      `);
      
      console.log('حالة عمود id بعد التعديل:');
      console.log(`- is_identity: ${verifyInfo.rows[0].is_identity === 'YES' ? 'YES' : 'NO'}`);
      console.log(`- column_default: ${verifyInfo.rows[0].column_default || 'NULL'}`);
      
    } catch (err) {
      await client.query('ROLLBACK');
      console.error('❌ حدث خطأ أثناء إصلاح العمود:', err.message);
    }
    
  } catch (err) {
    console.error('❌ خطأ في قاعدة البيانات:', err.message);
  } finally {
    client.release();
  }
}

// تنفيذ الوظيفة
fixCustomersTable()
  .then(() => {
    console.log('🏁 اكتمل تنفيذ السكريبت');
    // إغلاق الاتصال بعد الانتهاء
    setTimeout(() => {
      pool.end();
      process.exit(0);
    }, 1000);
  })
  .catch(err => {
    console.error('❌ خطأ غير متوقع:', err);
    process.exit(1);
  });